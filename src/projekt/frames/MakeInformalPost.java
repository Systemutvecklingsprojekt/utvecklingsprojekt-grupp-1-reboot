    private void jbAddTagActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAddTagActionPerformed
        // TODO add your handling code here:
        
        String tag = cbTags.getSelectedItem().toString();
        if(jtfNewTag.isEnabled()){    
            while(maxTags > 0){
                String newTag = jtfNewTag.getText();
                String oldTag = jLabel1.getText();
                if(maxTags == 1){
                    jLabel1.setText(oldTag + newTag);
                    maxTags--;
                    break;
                }
                else{
                    jLabel1.setText(oldTag + newTag + ", ");
                    maxTags--;
                    break;
                }
            }
        } else if(!jtfNewTag.isEnabled()) {
            while(maxTags > 0){
                String combTag = tag;
                String oldTag = jLabel1.getText();
                if(maxTags == 1){
                    jLabel1.setText(oldTag + combTag);
                    maxTags--;
                    jtfNewTag.setEnabled(true);
                    break;
                }
                else{
                    jLabel1.setText(oldTag + combTag + ", ");
                    maxTags--;
                    jtfNewTag.setEnabled(true);
                    break;
                }
            }
        }
    }//GEN-LAST:event_jbAddTagActionPerformed

