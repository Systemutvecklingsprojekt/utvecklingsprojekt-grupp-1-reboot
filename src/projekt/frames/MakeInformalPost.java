    private void fillTags(){
        ArrayList<String> tags = new ArrayList<>();
        tags.add("AW");
        tags.add("Vänner");
        tags.add("Solsken");
        
        for(String tag : tags){
            cbTags.addItem(tag);
        }
    }
    
    
    

